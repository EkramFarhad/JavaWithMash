package StringType;

public class StringSpecialCharacters {

    public static void main(String[] args) {

        //how to add a name inside a quote
        System.out.println("Hello \"Ekram\"");

        System.out.println("----------------");

        // how to add a name inside backslash
        System.out.println("c: \\Windows\\ ....");
        System.out.println("-------------------");

        // how to add a name in a new line

        System.out.println("Hello \nfarhad");
        System.out.println("----------------");

        // how to add tab
        System.out.println("ekram\tfarhad");
    }
}
